// Словарь глифов v0 (Р24 состав стихий, Р25 глаголы). Состав фиксирован,
// формы — выбор реализации (Р24: «формы глифов можно улучшать»).

// Стихии-существительные (Р24). Пентаграмма контрпиков живёт в spellcraft.
export type Element = 'fire' | 'water' | 'air' | 'earth' | 'lightning';

// Глаголы-модификаторы формы действия (Р25).
export type Verb = 'shield' | 'heal' | 'buff' | 'debuff';

// Любой распознаваемый через $P глиф. Замыкающий круг (Р27) — НЕ глиф,
// он детектируется отдельно (см. circle.ts).
export type Glyph = Element | Verb;

export const ELEMENTS: readonly Element[] = ['fire', 'water', 'air', 'earth', 'lightning'];
export const VERBS: readonly Verb[] = ['shield', 'heal', 'buff', 'debuff'];
export const GLYPHS: readonly Glyph[] = [...ELEMENTS, ...VERBS];

// Выбор форм v0 (обоснование — в описании PR). Ключевое: формы должны быть
// взаимно различимы простым point-cloud матчером и покрывать корпус Р32.
//   fire      🔥 — треугольник вершиной вверх (замкнут)
//   water     💧 — горизонтальная волна, 2 гребня (открыта)
//   air       🌪 — вертикальная S-серпантина, 1 период (открыта)
//   earth     ⛰ — квадрат (замкнут)
//   lightning ⚡ — острый зигзаг N, 3 сегмента (открыт)
//   shield    ⌒ — одиночная дуга-арка (открыта)
//   heal         — спираль внутрь, ~2 оборота (открыта)
//   buff      /  — диагональ снизу-слева вверх-вправо
//   debuff    \  — диагональ сверху-слева вниз-вправо
//
// Конфликт «спираль-вихрь-воздух ↔ спираль-хил» (Р25) разрешён в пользу
// различимости (требование задачи): воздух получил S-серпантину, спираль
// осталась только за хилом. $P ротационно-чувствителен — этим `/`≠`\`
// и горизонтальная волна ≠ вертикальная S различаются по ориентации.
